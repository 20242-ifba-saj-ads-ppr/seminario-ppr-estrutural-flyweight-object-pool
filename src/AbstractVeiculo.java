public abstract class AbstractVeiculo {
    
    abstract float calculaPrecoDiaria();

    abstract public String getMarca();
    abstract public String getModelo();
    abstract public String getDescricao();
    abstract public float getPrecoBase();

    abstract public String getPlaca();
    abstract public String getChassi();
    abstract public String getCor();
    abstract public int getQuilometragem();
    abstract public int getAno();




}
